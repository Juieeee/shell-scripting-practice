# AWK Practice

## Overview

This folder contains hands-on practice of the awk command in Linux.

awk is a powerful text-processing tool used to extract columns, filter data, and generate reports. It is widely used in DevOps for log analysis, parsing command outputs, and processing structured text files.

---

## Files

- employees.txt - Sample employee data
- awk_commands.sh - Shell script demonstrating various awk commands

---

## Commands Practiced

- Print specific columns
- Print multiple columns
- Filter rows based on conditions
- Display records with salary greater than a value
- Count total records using NR

---

## Real-world DevOps Use Cases

- Parse log files
- Extract fields from command output
- Analyze CSV/text files
- Generate reports
- Filter monitoring data

